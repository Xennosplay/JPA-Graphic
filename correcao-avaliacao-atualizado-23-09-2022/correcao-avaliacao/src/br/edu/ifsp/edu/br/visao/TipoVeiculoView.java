    private void bGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bGravarActionPerformed
        // TODO add your handling code here:
        
        //Inserindo
        String txtnome = tfNome.getText();
        if (txtnome.length() > 3){
            BigDecimal valorDiaria = new BigDecimal(tfValorDiaria.getText());
            TipoVeiculo tipoVeiculo = new TipoVeiculo(txtnome, valorDiaria);
            try{
                tipoVeiculoDAO.inserir(tipoVeiculo);
                JOptionPane.showMessageDialog(null, "Tipo de Veículo Cadastrado");
                
                this.setEnabledBototes(true);
                this.setEnableCampoTexto(false);
                
            } catch(Exception ex){
                JOptionPane.showMessageDialog(null, ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
            }
        }
        else{
                JOptionPane.showMessageDialog(null,
                        "Informe ao menos 4 caracteres no campo Nome",
                        "Atenção", JOptionPane.WARNING_MESSAGE);

        }
        
        
    }//GEN-LAST:event_bGravarActionPerformed

    private void bPesquisaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bPesquisaActionPerformed
        // TODO add your handling code here:
        tipoVeiculoDAO.findByNome(tfPesquisa.getText());
        this.atualizarTabela();
        
    }//GEN-LAST:event_bPesquisaActionPerformed

    private void atualizarTabela(){
        
        DefaultTableModel modelo = (DefaultTableModel) jTabela.getModel();
        modelo.setNumRows(0);
        for (TipoVeiculo tipoVeiculo : tiposVeiculos) {
            modelo.addRow(new Object[] {tipoVeiculo.getId(),
                tipoVeiculo.getNome(), tipoVeiculo.getValorDiaria()});
        }
    }
    
