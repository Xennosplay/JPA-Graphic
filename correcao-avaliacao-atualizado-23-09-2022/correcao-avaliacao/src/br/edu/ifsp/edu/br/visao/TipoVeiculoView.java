    private void bAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bAlterarActionPerformed
        // TODO add your handling code here:
        
        if(jTabela.getSelectedRow() >= 0){
            this.setEnableCampoTexto(true);
            this.setEnabledBototes(false);
        
            //Carregar os dados para os campos de texto
            
            tfNome.setRequestFocusEnabled(true);
        }
        else{
            JOptionPane.showMessageDialog(null,
                    "Selecione um Tipo de Veículo",
                    "Atençao", JOptionPane.WARNING_MESSAGE);
        }
        
        
    }//GEN-LAST:event_bAlterarActionPerformed

